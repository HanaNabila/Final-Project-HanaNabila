
public class Coba {
	String lala;
	String atip;
	@Override
	public String toString() {
		return "Coba [lala=" + lala + ", atip=" + atip + "]";
	}
	
	
	public Coba(String lala, String atip) {
		super();
		this.lala = lala;
		this.atip = atip;
	}


	public String getLala() {
		return lala;
	}


	public void setLala(String lala) {
		this.lala = lala;
	}


	public String getAtip() {
		return atip;
	}


	public void setAtip(String atip) {
		this.atip = atip;
	}

	
	
	
	

}
